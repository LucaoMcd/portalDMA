<div class="article-content" style="font-size: 13px; line-height: 18px; color: #56802d; margin-top: 0px; font-family: 'Helvetica Neue', Arial, Helvetica, Geneva, sans-serif;" align="left">
<p style="margin-bottom: 15px; text-align: justify;">DMA/SIN convida comunidade da UFRN para edi&ccedil;&atilde;o 2013.1 da CESTA Ecol&oacute;gica.</p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">A Diretoria de Meio Ambiente &ndash; DMA, integrada &agrave; Superintend&ecirc;ncia de Infraestrutura &ndash; SIN, da Universidade Federal do Rio Grande do Norte &ndash; UFRN, desenvolve diversos projetos e a&ccedil;&otilde;es para incentivar o processo cont&iacute;nuo de constru&ccedil;&atilde;o de valores fundamentados na ideia do equil&iacute;brio ambiental, da qualidade de vida e da sustentabilidade. Um desses projetos &eacute; a CESTA Ecol&oacute;gica, que consiste em um balaio de eventos ambientais realizado no final de cada semestre letivo, no Centro de Conviv&ecirc;ncia.</span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Ao promover a CESTA Ecol&oacute;gica divulgando os trabalhos desenvolvidos pela DMA/SIN para transformar a Universidade em uma Institui&ccedil;&atilde;o cada vez mais sustent&aacute;vel, o objetivo da DMA &eacute; promover viv&ecirc;ncias e a integra&ccedil;&atilde;o entre os diversos setores da UFRN, estimulando o debate e a constitui&ccedil;&atilde;o de parcerias internamente. Al&eacute;m dos muros da Institui&ccedil;&atilde;o, a a&ccedil;&atilde;o visa ampliar a visibilidade sobre a quest&atilde;o ambiental, consolidando o papel de formadora de opini&atilde;o da Universidade. </span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;"><strong>Hist&oacute;rico</strong></span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">A CESTA Ecol&oacute;gica acontece, desde 2009, em dois momentos durante o ano, concentrados em um dia coincidindo com cada final de semestre letivo. Na primeira edi&ccedil;&atilde;o anual, acontece vinculada &agrave; Semana de Meio Ambiente da UFRN - SeMA, em junho. A segunda vers&atilde;o volta-se para o encerramento das atividades letivas, no Campus, em novembro.</span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">A proposta de multi-eventos em um mesmo espa&ccedil;o foi pensada para promover a reflex&atilde;o sobre a quest&atilde;o ambiental de diferentes formas. Institucionalizada como projeto junto &agrave; Pr&oacute;-reitoria de Extens&atilde;o da UFRN, a CESTA Ecol&oacute;gica j&aacute; promoveu diversos tipos de atividades ao longo desses quatro anos.</span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;"><strong>Edi&ccedil;&atilde;o 2013.1</strong></span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Produzida pela DMA Comunica, programa de comunica&ccedil;&atilde;o e sensibiliza&ccedil;&atilde;o ambiental da DMA, a edi&ccedil;&atilde;o desse semestre da CESTA Ecol&oacute;gica ser&aacute; realizada entre os dias 5 e 7 de junho, dentro da programa&ccedil;&atilde;o da SeMA, da UFRN, que acontece de 3 a 7 de junho. </span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Nesta edi&ccedil;&atilde;o ser&aacute; lan&ccedil;ada a campanha carro-chefe da DMA, desenvolvida pela DMA Comunica, atrav&eacute;s do projeto Exposi&ccedil;&otilde;es Itinerantes e Interativas &ndash; EXPO I.I.: &ldquo;Qu&iacute;micos, N&oacute;s Precisamos. Como Utilizamos?&rdquo; A CESTA Ecol&oacute;gica trar&aacute; outras campanhas associadas, como a coleta de medicamentos vencidos (NUPLAM) e de banners (Dept. de Hist&oacute;ria), para reutiliza&ccedil;&atilde;o. Exposi&ccedil;&otilde;es diversas, distribui&ccedil;&atilde;o de mudas, rodas de conversa, exibi&ccedil;&atilde;o de v&iacute;deos, apresenta&ccedil;&otilde;es institucionais e trabalhos de estudantes com a tem&aacute;tica ambiental, tamb&eacute;m far&atilde;o parte do evento. A programa&ccedil;&atilde;o ser&aacute; encerrada &agrave;s 14:00h da sexta-feira, 7, com apresenta&ccedil;&otilde;es culturais - musical e mamulengo - gratuitas, para os participantes do Centro de Coviv&ecirc;ncia da UFRN, a partir das 11:30h.</span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;"><strong>Junte-se ao projeto</strong></span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">A DMA/SIN convida a comunidade universit&aacute;ria a avaliar as possibilidades do seu trabalho, projeto ou setor, e a participar desse grande encontro para reflex&atilde;o da tem&aacute;tica ambiental. </span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Abaixo, algumas das atividades realizadas em edi&ccedil;&otilde;es anteriores. </span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Exposi&ccedil;&otilde;es: fotogr&aacute;ficas, tem&aacute;ticas, de objetos, utilit&aacute;rios e arte com materiais reaproveitados; plantas nativas, divulga&ccedil;&atilde;o cient&iacute;fica, trabalhos acad&ecirc;micos, banners institucionais, projetos e experimentos de pesquisa; lan&ccedil;amento de livros, recitais, performances teatrais, interven&ccedil;&otilde;es diversas (enquetes, demonstra&ccedil;&atilde;o do uso dos coletores, jogos educativos, c&aacute;lculo da pegada ecol&oacute;gica), apresenta&ccedil;&otilde;es musicais e art&iacute;sticas, vitrines ambientais, sorteio de livros, entre outras&hellip;</span></p>
<p style="margin-bottom: 15px; text-align: justify;"><span style="color: #008000;">Para participar, envie mensagem para J&ocirc; Carvalho - <em>dmacomunica@infra.ufrn.br</em>, propondo colabora&ccedil;&atilde;o.</span></p>
