                      <p class="style16">CIRCUITO  TELA VERDE - 1<u>a </u>MOSTRA NACIONAL DE PRODU&Ccedil;&Atilde;O AUDIOVISUAL  INDEPENDENTE DE V&Iacute;DEOS SOCIOAMBIENTAIS</p>
                    </div>
                    <p align="justify" class="style16"> Estudo do F&oacute;rum Humanit&aacute;rio Global, entidade das  Na&ccedil;&otilde;es Unidas, divulgados na sexta-feira, 29 de junho, indicam que a mudan&ccedil;a  clim&aacute;tica mata cerca de 315 mil pessoas por ano, de fome, doen&ccedil;as ou desastres  naturais, e o n&uacute;mero deve subir para 500 mil at&eacute; 2030, e afetar&aacute; 10% da  popula&ccedil;&atilde;o mundial da atualidade (6,7 bilh&otilde;es).&ldquo;Os primeiros atingidos e os mais  afetados s&atilde;o os grupos mais pobres do mundo, embora eles pouco tenham feito  para causar o problema&rdquo;. Disse Kofi Annan, presidente da entidade.<br />
                      <br />
O  CIRCUITO TELA VERDE, 1<u>a</u> Mostra Nacional de Produ&ccedil;&otilde;es  Audiovisuais com Tem&aacute;tica socioambiental, que ser&aacute; exibido no per&iacute;odo de 2 a 5  de junho na UFRN, atrav&eacute;s da sua Sala verde, re&uacute;ne 30 v&iacute;deos de curta metragem  que trazem uma importante contribui&ccedil;&atilde;o para compreender como as comunidades  podem participar nos processos da gest&atilde;o ambiental p&uacute;blica.<br />
<br />
Produzidos  como parte de um processo de educa&ccedil;&atilde;o ambiental, de car&aacute;ter cr&iacute;tico e  participativo, com envolvimento direto das comunidades na produ&ccedil;&atilde;o dos v&iacute;deos,  utilizando metodologias de educomunica&ccedil;&atilde;o, visa contribuir com a constru&ccedil;&atilde;o de  valores culturais comprometidos com a qualidade ambiental. <br />
<br />
O  Circuito tela Verde, nesta primeira vers&atilde;o experimental, inaugura uma parceria  in&eacute;dita entre os Minist&eacute;rios da Cultura e do Meio Ambiente, e entre estes e  espa&ccedil;os educadores como Salas Verdes, Pontos de Cultura e Coletivos educadores.<br />
<br />
