                      <div align=center class=style39>
                        <div align=left><span class=style16>DMA/SIN ENCERRA ANO COM SEXTA ECOL&Oacute;GICA NO CENTTRO DE CONVIV&Ecirc;NCIA </span></div>
                      </div>
                      <p align=justify class=style16> Com o tema <em>SUSTENTABILIDADE: Voc&ecirc; Perguntou, Eis Aqui as Respostas</em>, a Sexta Ecol&oacute;gica de fim de ano convida &agrave; reflex&atilde;o sobre nosso modo de vida, respondendo &agrave;s quest&otilde;es colocadas em seus pain&eacute;is interativos. Os pain&eacute;is foram expostos durante a primeira Sexta Ecol&oacute;gica, na Semana de Meio Ambiente, em junho deste ano, e percorreram diversos setores da UFRN at&eacute; a exibi&ccedil;&atilde;o na CIENTEC.  Mais do que respostas acabadas, a exposi&ccedil;&atilde;o itinerante buscou a participa&ccedil;&atilde;o e di&aacute;logo com a comunidade universit&aacute;ria j&aacute; que &quot;sustentabilidade&quot; &eacute; um tema que que n&atilde;o se elucida t&atilde;o f&aacute;cil, aglutinando v&aacute;rias possibilidades de abordagens e questionamentos. </p>
                      <p align=justify class=style16>O encerramento do ciclo itinerante da exposi&ccedil;&atilde;o, nesta &uacute;ltima Sexta Ecol&oacute;gica de 2009, acontece em um momento bastante significativo. A partir de hoje, os olhos do mundo estar&atilde;o voltados para a reuni&atilde;o de c&uacute;pula da ONU (Organiza&ccedil;&atilde;o das Na&ccedil;&otilde;es Unidas) sobre mudan&ccedil;as clim&aacute;ticas,   destinada a discutir metas para reduzir as emiss&otilde;es de gases do efeito estufa,    em Copenhagen, na Dinamarca. Ser&aacute; a maior reuni&atilde;o j&aacute; realizada sobre o aquecimento global, reunindo cerca de 15 mil   pessoas, envolvendo delegados de 192 pa&iacute;ses, ambientalistas e ONGs (Fonte: Estad&atilde;o). Com o prop&oacute;sito de superar a   grande brecha entre pa&iacute;ses ricos e pobres para combater os efeitos das emiss&otilde;es   de CO2 nas pr&oacute;ximas d&eacute;cadas, reuni&otilde;es como estas envolvem diretamente a quest&atilde;o da sustentabilidade, atravessando as vertentes ambiental, social e econ&ocirc;mica. </p>
                      <p align=justify class=style16>No intuito de contribuir  com a discuss&atilde;o visando a conquista de um modelo de desenvolvimento baseado em uma sustentabilidade  poss&iacute;vel   , as institui&ccedil;&otilde;es de ensino e formadoras de opini&atilde;o exercem um papel fundamental.   </p>
                      <p align=justify class=style16>  A UFRN/SIN/DMA desenvolve v&aacute;rios projetos e a&ccedil;&otilde;es durante todo o ano, incentivando a mudan&ccedil;a de  comportamentos rumo ao desenvolvimento de uma sociedade com novos valores. Atrav&eacute;s de um processo   cont&iacute;nuo de constru&ccedil;&atilde;o de  conhecimento, as a&ccedil;&otilde;es e pr&aacute;ticas ambientais e socioecon&ocirc;micas mais respons&aacute;veis contribuem com a dissemina&ccedil;&atilde;o desses valores. A Sexta Ecol&oacute;gica &eacute; um desses projetos, e dedica-se exatamente &agrave; tarefa de presta&ccedil;&atilde;o de contas do trabalho realizado, al&eacute;m de promover um dia de exposi&ccedil;&otilde;es e atividades voltadas para a reflex&atilde;o sobre o meio ambiente. </p>
                      <p align=justify class=style16>O projeto  Sexta Ecol&oacute;gica acontece uma vez, a cada semestre,  no Centro de Conviv&ecirc;ncia.</p>
                      <p align=justify class=style16>Neste final de ano, estaremos promovendo uma grande festa junto a diversos outros setores da UFRN. Al&eacute;m do NAC,  parceiro constante da DMA/SIN atrav&eacute;s do projeto Conviver, teremos tamb&eacute;m lan&ccedil;amento de livros e a participa&ccedil;&atilde;o do DDRH. </p>
                      <p align=justify class=style16>A partir de amanh&atilde; ser&aacute; poss&iacute;vel conferir a programa&ccedil;&atilde;o completa da Sexta Ecol&oacute;gica e do Conviver no <a href=http://www.meioambiente.ufrn.br class=style9>Portal de Meio Ambiente da UFRN</a>. </p>
