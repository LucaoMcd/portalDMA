                          <p class=style16><br>
                            SEXTA  Ecol&oacute;gica APRESENTA TEATRO DE INTERVEN&Ccedil;&Atilde;O </p>
                          <p align=justify class=style16> Ao longo do ano, a DMA / SIN conta com diversos parceiros e colaboradores em suas atividades para trazer a quest&atilde;o ambiental &agrave; pauta de discuss&atilde;o pela comunidade universit&aacute;ria. <br>
                            <br>
                            Conhe&ccedil;a o que &eacute; e quem faz  <em>Tillandsia</em>. </p>
                          <p align=justify class=style16><em>Tillandsia</em> surgiu como mais uma personagem ecol&oacute;gica,  para falar &agrave;s pessoas sobre a necessidade da preserva&ccedil;&atilde;o ambiental, mostrando-se eficiente pelo formato com a&ccedil;&atilde;o r&aacute;pida e interativa, abordando quest&otilde;es do cotidiano a partir de personagens caricatos. Seu nome foi baseado em uma brom&eacute;lia end&ecirc;mica  da Mata Atl&acirc;ntica.<br>
                            <br>
  A id&eacute;ia dos personagens ecol&oacute;gicos veio, inicialmente, a partir do  projeto Nativas no Campus, cujo objetivo &eacute; trabalhar a sensibiliza&ccedil;&atilde;o ambiental tendo como foco principal, a preserva&ccedil;&atilde;o da Mata Atl&acirc;ntica. A primeira personagem, uma &ldquo;eco-chata&rdquo;, foi  desenvolvida para a Cientec em 2005. Para 2006, foi feita a &ldquo;Patr&iacute;cia&rdquo;, uma  patricinha ecologicamente incorreta, que saiu da UFRN para diversos eventos, muitos deles, por quest&otilde;es &oacute;bvias, em shoppings centers. Na Cientec de 2007, o projeto fez sua primeira aproxima&ccedil;&atilde;o com a DMA que, atrav&eacute;s de seu setor de Comunica&ccedil;&atilde;o, a DMA Comunica, comprou a id&eacute;ia das personagens ambientais. Fruto do trabalho conjunto  surgiu   &ldquo;Vict&oacute;ria&rdquo;, uma personagem que veio  de Portugal pela primeira vez, ao Brasil, em 1500, e retorna no presente,  abismada com o &quot;causticante aquecimento local da cidade do Natal...&quot; 2009 foi a vez de <em>Tillandsia, </em>concebida inicialmente para a primeira Sexta Ecol&oacute;gica, durante a Semana de Meio Ambiente, mas que s&oacute; veio a nascer durante a CIENTEC. </p>
                          <p align=justify class=style16>                            A cria&ccedil;&atilde;o das personagens e interpreta&ccedil;&atilde;o fica por  parte da bi&oacute;loga, aluna da p&oacute;s-gradua&ccedil;&atilde;o em Psicobiologia, Luiza Cervenka, que  acredita no teatro ecol&oacute;gico como forma l&uacute;dica de fazer chegar &agrave;s pessoas temas importantes.<br>
                            <br>
                            Quem quiser conhecer  <em>Tillandsia</em>, precisa apenas comparecer &agrave; Sexta Ecol&oacute;gica que amanh&atilde;, dia11 de dezembro,  inicia mais cedo, fruto de sua parceria com o NAC. O projeto sexta Ecol&oacute;gica acontece uma vez a cada semestre. </p>
