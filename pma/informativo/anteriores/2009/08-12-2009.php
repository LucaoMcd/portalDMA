                          <p class=style16>COLETA DE GARRAFAS PET:<br>
                            Quer nos Ajudar a Construir Uma &Aacute;rvore de Natal?<br>
                            <br>
                            PARTICIPE DA NOSSA CAMPANHA! </p>
                          </div>
                      </div>
                      <p align=justify class=style16>No pr&oacute;ximo dia 11 ser&aacute; realizada a &uacute;ltima SEXTA Ecol&oacute;gica do ano. Para nos ajudar a pensar uma sociedade com outros padr&otilde;es de consumo, estamos mais uma vez promovendo atividades para refletir sobre o nosso modo de vida e a quantidade de lixo que produzimos. Uma dessas atividades ser&aacute; a oficina para confec&ccedil;&atilde;o de  uma &aacute;rvore de Natal que far&aacute; o reaproveitamento de garrafas pet.<br>
                        <br>
                        Colabore com a campanha doando garrafas pet de 2 litros, verdes ou transparentes. As garrafas podem ser entregues na sala da DMA/SIN (primeiro andar, no mesmo pr&eacute;dio do Dept. de Artes) at&eacute; a 5a-feira, dia 10. </p>
                      <p align=justify class=style16>Contatos podem ser feitos no endere&ccedil;o: dmacomunica@infra.ufrn.br </p>
                      <p align=justify class=style16>ENTENDA A RAZ&Atilde;O DE PROMOVERMOS ESTES TIPOS DE ATIVIDADES </p>
                      <p align=justify class=style16>Cada brasileiro produz uma m&eacute;dia de 1,05kg de lixo, por dia. Pode-se calcular ent&atilde;o que cada um ir&aacute; produzir at&eacute; o fim de sua vida cerca de 25,68 toneladas. Onde colocar todo esse lixo? A resposta &eacute;: N&atilde;o existe onde. Dentro de poucos anos, se for mantida a produ&ccedil;&atilde;o atual de lixo, n&atilde;o haver&aacute; mais lugar onde colocar tanto lixo!<br>
                        <br>
                        A ado&ccedil;&atilde;o de uma postura baseada na sustentabilidade gera n&atilde;o apenas vantagens para a economia mas grandes ganhos na qualidade ambiental que t&ecirc;m reflexos na popula&ccedil;&atilde;o das cidades. Eis alguns exemplos:<br> 
                        <br>
                        Benef&iacute;cios ambientais da reciclagem<br>
                        <br>
                        50 kg de papel reciclado evitam o corte de uma &aacute;rvore de 7 anos. Cada tonelada de papel reciclado pode substituir o plantio de at&eacute; 350 m2 de monocultura de eucalipto. Uma tonelada de papel reciclado economiza 20 mil litros de &aacute;gua e 1.200 litros de &oacute;leo combust&iacute;vel.<br>
                        <br>
                        A reciclagem de vidro diminui a emiss&atilde;o de gases poluidores pelas f&aacute;bricas, aumenta a vida &uacute;til dos aterros sanit&aacute;rios e poupa a extra&ccedil;&atilde;o de min&eacute;rios como areia, barrilha, calc&aacute;rio, feldspato, etc...<br>
                        <br>
                        A reciclagem do pl&aacute;stico impede um enorme preju&iacute;zo ao meio ambiente, pois o material &eacute; muito resistente a radia&ccedil;&otilde;es, calor, ar e &aacute;gua.<br>
                        <br>
                        A cada quilo de alum&iacute;nio reciclado, 5 kg de bauxita (min&eacute;rio com que se produz o alum&iacute;nio) s&atilde;o poupados. A reciclagem de alum&iacute;nio economiza 95% da energia que seria usada para produzir alum&iacute;nio prim&aacute;rio.<br>
