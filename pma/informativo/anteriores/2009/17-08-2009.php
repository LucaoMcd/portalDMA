			    <p align="justify" class="style33">Projeto EXPEDI&Ccedil;&Ocirc;ES da DMA/SIN abre inscri&ccedil;&otilde;es para aula-passeio pelo Rio Potengi			    </p>
			    <p align="justify"><span class="style43">Atuando em parceria com o  projeto Chama Mar&eacute;, do IDEMA, o projeto Expedi&ccedil;&otilde;es da  DMA/SIN, levar&aacute; os alunos da UFRN  para o passeio de barco pelo Rio Potengi, no dia 28 de agosto. As vagas s&atilde;o limitadas a 50 pessoas. As inscri&ccedil;&otilde;es j&aacute; est&atilde;o abertas e s&atilde;o feitas pessoalmente, na pr&oacute;pria DMA, no per&iacute;odo da manh&atilde; - das 8:30 &agrave;s 11:30h, e &agrave; tarde, das 14:30 &agrave;s 17:30h. A DMA ocupa o 1<span class="style47">0</span> andar do pr&eacute;dio da <a href="http://www.infra.ufrn.br/" target="_blank" class="LinkNoTexto">SIN - Superintend&ecirc;ncia de  Infraestrutura</a>, ao lado do Dept de Artes.<br />
                    <br />
                </span><span class="style43"> Dada &agrave; limita&ccedil;&atilde;o do n&uacute;mero de vagas, a DMA pede que apenas efetue a inscri&ccedil;&atilde;o, quem puder garantir o comparecimento. </span> <span class="style43">Entre as informa&ccedil;&otilde;es pessoais, ser&atilde;o necess&aacute;rios o n&uacute;mero do RG e CPF.</span> <span class="style43">A sele&ccedil;&atilde;o para o passeio ser&aacute; feita pela ordem de inscri&ccedil;&atilde;o.</span></p>
			    <p align="justify"><span class="style42">&Aacute;gua para que.<br />
			        <br />
			      </span><span class="style43">A  ingest&atilde;o de &aacute;gua pura &eacute; um  dos mais importantes fatores para a conserva&ccedil;&atilde;o da sa&uacute;de, preven&ccedil;&atilde;o das  doen&ccedil;as e prote&ccedil;&atilde;o do organismo contra o envelhecimento. Apesar de sua import&acirc;ncia, temos s&eacute;rios problemas de gerenciamento deste recurso. Pesquisa realizada pelo IBGE - Instituto Brasileiro de Geografia e Estat&iacute;stica  alerta: o prec&aacute;rio  saneamento b&aacute;sico &eacute; respons&aacute;vel por 80 % das doen&ccedil;as que afetam a  popula&ccedil;&atilde;o e 65 % das interna&ccedil;&otilde;es hospitalares de crian&ccedil;as. E mais: <br />
			      <br />
			      - 30 milh&otilde;es de habitantes dos 150 milh&otilde;es do Brasil n&atilde;o recebem &aacute;gua tratada. <br />
			      - 92 % do esgoto produzido no pa&iacute;s &eacute; lan&ccedil;ado nos rios e no mar sem qualquer tratamento. <br />
                  <br />
                  O <em>Rio Potengi</em> vive uma situa&ccedil;&atilde;o cada vez mais dram&aacute;tica devido ao lan&ccedil;amento de efluentes de diversos tipos que  s&atilde;o descartados em seu leito desde a &eacute;poca do surgimento da cidade de Natal. A cria&ccedil;&atilde;o  de bairros populosos e sem  infra-estrutura de saneamento b&aacute;sico, juntamente com a instala&ccedil;&atilde;o de atividades  industriais nas margens do rio, intensificou a carga de poluentes lan&ccedil;ados  diretamente no rio, muitas vezes <em>in natura</em>, contribuindo para o  agravamento no quadro de degrada&ccedil;&atilde;o e polui&atilde;o ambiental. </span></p>
			    <p align="justify" class="style43">Conhecer a nossa realidade s&oacute;cio-econ&ocirc;mico-ambiental &eacute; o primeiro passo na preserva&ccedil;&atilde;o de uma boa qualidade de vida. O projeto <em>Expedi&ccedil;&otilde;es</em> foi  criado pela DMA/SIN para promover passeios, trilhas fotogr&aacute;ficas e visitas a institui&ccedil;&otilde;es  vinculadas ao meio ambiente,  com o  intuito de sensibilizar quanto &agrave;s quest&otilde;es ambientais. O barco <em>Chama Mar&eacute;</em>, projeto do IDEMA em parceria com a UNP, &eacute; um espa&ccedil;o pedag&oacute;gico para realiza&ccedil;&atilde;o de aulas-passseio no sentido de promover a educa&ccedil;&atilde;o ambiental voltada para o debate sobre o rio Potengi. <br />
			      <br />
			      <strong>SUSTENTABILIDADE percorre setores do Campus<br />
			      <br />
			      </strong>Ap&oacute;s exibi&ccedil;&atilde;o no sagu&atilde;o da Escola de M&uacute;sica, esta semana ser&aacute; a vez do Restaurante Universit&aacute;rio receber a Exposi&ccedil;&atilde;o sobre Sustentabilidade.<strong> </strong>A exposi&ccedil;&atilde;o que visa questionar nosso atual modo de vida e fazer refletir sobre formas sustent&aacute;veis de viver, estar&aacute; sendo exibida no per&iacute;odo de 18 a 21 de agosto. No painel de abertura da exposi&ccedil;&atilde;o  o visitante pode opinar sobre o tema. Setores interessados na exibi&ccedil;&atilde;o poder&atilde;o solicit&aacute;-la &agrave; DMA Comunica, nos telefones 3215 3161 ou 3162, ramal 234.</p>
			    <p align="justify"><span class="style43"><strong>Acompanhe os programas e a&ccedil;&otilde;es da DMA no <a href="http://www.meioambiente.ufrn.br/" target="_blank">Portal de Meio Ambiente da UFRN</a>. </strong><br />
			        <br />
			        <strong>DMA / SIN</strong><br />
			        Estruturada junto &agrave; <strong>SIN </strong>- Superintend&ecirc;ncia de Infraestrutura, a <strong> DMA</strong> &eacute; respons&aacute;vel pela execu&ccedil;&atilde;o dos programas de  planejamento ambiental da UFRN, entre os quais o <em>Pro&Aacute;GUA</em> - conjunto de atividades desenvolvidas  para garantir a qualidade da &aacute;gua fornecida &agrave; comunidade  universit&aacute;ria, o programa de arboriza&ccedil;&atilde;o - <em>Pro&Aacute;RVORE</em>, e o <em>ProGIRES</em> - Programa de Gest&atilde;o  Integrada de Res&iacute;duos. A  DMA conta ainda com os programas de Comunica&ccedil;&atilde;o <em>(DMA Comunica</em>) e  Educa&ccedil;&atilde;o (<em>ProEA</em>), envolvidos com a promo&ccedil;&atilde;o de  atividades voltadas  para a sensibiliza&ccedil;&atilde;o e educa&ccedil;&atilde;o ambiental.</span><br />
