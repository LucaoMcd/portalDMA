                      <p align="justify" class="style16">No fim da d&eacute;cada de 1970, ainda menina, Du&iacute;lia de Mello encantava-se com as   descobertas realizadas pelas sondas da Nasa, a ag&ecirc;ncia espacial americana.   &quot;Queria entender como o universo funcionava t&atilde;o bem sendo t&atilde;o complexo&quot;, diz.   Essa precoce curiosidade sobre a noite escura parecia profetizar que seu futuro   estava escrito nas estrelas. </p>
