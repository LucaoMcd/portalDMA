                      <p align="justify" class="style16">No <em>blog</em>   <strong>Mulher das   Estrelas</strong> Du&iacute;lia fala sobre os bastidores da ag&ecirc;ncia espacial   norte-americana e sobre como &eacute; trabalhar com alguns dos mais importantes   cientistas do mundo. Apesar de ter encerrado as postagens recentemente, muito de seu trabalho pode ser consultado no endere&ccedil;o <span class="style22">super.abril.com.br/blogs/mulherestrelas. </span></p>
