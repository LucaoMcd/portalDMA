			    <p align="justify" class="style33">DIA 22 DE MAR&Ccedil;O &Eacute; O DIA DA &Aacute;GUA </p>
			    <p align="justify" class="style16"><strong>Porqu&ecirc; um Dia para Celebrarmos a &Aacute;gua?<br />
			    </strong>Mas porque a ONU se preocupou com a &aacute;gua se sabemos que dois ter&ccedil;os do planeta   Terra &eacute; formado por este precioso l&iacute;quido? A raz&atilde;o &eacute; que pouca quantidade, cerca   de 0,008 %, do total da &aacute;gua do nosso planeta &eacute; pot&aacute;vel (pr&oacute;pria para o   consumo). E como sabemos, grande parte das fontes desta &aacute;gua (rios, lagos e   represas) esta sendo contaminada, polu&iacute;da e degradada pela   a&ccedil;&atilde;o predat&oacute;ria do homem. Esta situa&ccedil;&atilde;o &eacute; preocupante, pois poder&aacute; faltar, num   futuro pr&oacute;ximo, &aacute;gua para o consumo de grande parte da popula&ccedil;&atilde;o mundial.   Pensando nisso, foi institu&iacute;do o Dia Mundial da &Aacute;gua, cujo objetivo principal &eacute;   criar um momento de reflex&atilde;o, an&aacute;lise, conscientiza&ccedil;&atilde;o e elabora&ccedil;&atilde;o de medidas   pr&aacute;ticas para resolver tal problema.<br />
			      <br />
			      No dia 22 de mar&ccedil;o de 1992, a ONU   tamb&eacute;m divulgou um importante documento: a &ldquo;Declara&ccedil;&atilde;o Universal dos Direitos da   &Aacute;gua&rdquo;. Este texto apresenta uma s&eacute;rie de medidas, sugest&otilde;es e   informa&ccedil;&otilde;es que servem para despertar a consci&ecirc;ncia ecol&oacute;gica da popula&ccedil;&atilde;o e dos   governantes para a quest&atilde;o da &aacute;gua.<br />
			      <br />
			      Em 22 de dezembro de 1992, a Assembl&eacute;ia Geral da  ONU declarou que no dia 22 de mar&ccedil;o de cada ano, a partir de 1993, seria  celebrado o Dia Mundial da &Aacute;gua. Tal decis&atilde;o baseou-se nas recomenda&ccedil;&otilde;es  contidas no cap&iacute;tulo 18 da Agenda 21, no qual se define que o objetivo  geral &eacute; assegurar que se mantenha uma oferta adequada de &aacute;gua de boa qualidade  para toda a popula&ccedil;&atilde;o do planeta, ao mesmo tempo em que se preserve as fun&ccedil;&otilde;es  hidrol&oacute;gicas, biol&oacute;gicas e qu&iacute;micas dos ecossistemas.<br />
			      Com a institui&ccedil;&atilde;o do Dia Mundial da &Aacute;gua, os pa&iacute;ses  foram convidados a se dedicar &agrave; data, a aderir &agrave;s recomenda&ccedil;&otilde;es da ONU  relativas aos recursos h&iacute;dricos e a concretizar atividades apropriadas ao contexto  de cada pa&iacute;s. (Fonte: MMA) <br />
			      <br />
			      <strong>Dia Nacional  da &Aacute;gua</strong><br />
			      No Brasil a ades&atilde;o partiu do Congresso Nacional. A  Lei n&ordm; 10.670, de 14 de maio de 2003, instituiu o Dia Nacional da &Aacute;gua, que  tamb&eacute;m passou a ser comemorado no dia 22 de mar&ccedil;o de cada ano, simultaneamente  &agrave; data mundial.</p>
			    <p align="justify" class="style16">Em Natal, a UFRN conta com um setor respons&aacute;vel pela elabora&ccedil;&atilde;o e execu&ccedil;&atilde;o de projetos e obras de edifica&ccedil;&otilde;es, a <strong><a href="http://www.infra.ufrn.br/" target="_blank" class="LinkNoTexto">SIN - Superintend&ecirc;ncia de  Infra-estrutura</a></strong>, a qual se vincula a <strong>DMA </strong>- Divis&atilde;o de Meio Ambiente. A DMA &eacute; diretamente respons&aacute;vel pelos programas de  planejamento ambiental da UFRN. Entre eles, o <em>Pro&Aacute;GUA</em> consiste num conjunto de atividades desenvolvidas  de forma cont&iacute;nua para garantir a qualidade da &aacute;gua fornecida &agrave; comunidade  universit&aacute;ria. </p>
			    <p align="justify" class="style35">DICA ProAGUA: VOC&Ecirc; SABIA...</p>
			    <p align="justify" class="style35">... que &eacute; poss&iacute;vel   economizar &aacute;gua na sua caixa de descarga acoplada?</p>
			    <p class="style16">- o n&iacute;vel de sua caixa de descarga acoplada pode ser regulada em dois n&iacute;veis.<br />
			      - regule o n&iacute;vel de &aacute;gua na caixa pelo &quot;copo-b&oacute;ia&quot;. Girando-o no sentido da haste, aumenta-se o n&iacute;vel da &aacute;gua. Para economizar diminuindo  o n&iacute;vel de &aacute;gua na caixa, gire o copo-b&oacute;ia no sentido contr&aacute;rio.<br />
